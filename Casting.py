strNum = "100"
intNum = 100;
floatNum = 99.99;

# Print value
print(strNum);
print(intNum);
print(floatNum);

# check Type

print(type(strNum));
print(type(intNum));
print(type(floatNum));

# string to integers

stToInt = int(strNum);
print(stToInt)
print(type(stToInt));

# string to float
stToFl = float(strNum);
print(stToFl);
print(type(stToFl));

# int to string

intToStr = str(intNum);
print(intToStr);
print(type(intToStr));

# int to float

intToFloat = float(intNum);
print(intToFloat);
print(type(intToFloat));

# float to string

floatToStr = str(floatNum);
print(floatToStr);
print(type(floatToStr));

# float to int

floatToInt = int(floatNum);
print(floatToInt);
print(type(floatToInt))
